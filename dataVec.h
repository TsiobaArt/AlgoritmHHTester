void sortPoints(std::vector<Point> &points)
{
    std::sort(points.begin(), points.end(), [](const Point &p1, const Point &p2) {
        if (p1.lat == p2.lat) {
            return p1.lon < p2.lon;
        }
        return p1.lat < p2.lat;
    });
}

